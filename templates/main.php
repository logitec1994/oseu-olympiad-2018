        <p>Не следует пренебрегать помощью окружающих. Задавая всем вопрос о том, как они определились с выбором профессии, можно узнать много нового и интересного.</p>
        <p>Родители и другие родственники должны поделиться собственным опытом, но не приукрашивая действительность, а честно объясняя на кого они учились, стали ли специалистом в выбранной области или в итоге пришли к чему-то другому.</p>
        <p>Очень часто советы окружающих позволяют подростку сформировать свое собственное мнение.</p>
        <p>И если взрослые будут терпеливы, честны и внимательны, то в будущем ребенок сможет реализовать себя в деле, которое ему действительно нравится.</p>
        <p>Ведь гораздо сложнее искать свою дорогу в жизни, будучи уже состоявшейся личностью, которая не получила поддержки в столь трудном возрасте.</p>
