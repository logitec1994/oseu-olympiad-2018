        Наш ресурс - ознакомительная платформа для абитуриентов/будущих студентов. Мы разработали алгоритмы адаптивного ознакомления с электронным оформлением документов для поступления в университет, помогаем выбрать направления и определится со специальностью. С помощью игры в виде квестов, каждый абитуеринт может узнать необходимую информацию для поступление в наш вуз.
        <h3>Почему важно выбирать профессию заранее?</h3>
        <p>Прежде всего необходимо понимать, что работа — это неотъемлемая часть жизни каждого человека, но не первостепенное.</p>
        <p>Поскольку в среднем каждый житель планеты тратит на работу без малого 40–50 часов в неделю, важно подобрать себе такое дело, которое не будет вызывать дискомфорта, внутреннего противоречия и отторжения.</p>
        <h4>На чем же должен основываться выбор будущей специальности?</h4>
        <p>Почему необходимо определиться с профессией
            Очень часто от старшеклассников можно услышать: «Куда мне торопиться? Никто из моих друзей еще не выбрал универ. Вот закончу школу, и все решу».</p>
        <p>К сожалению, это в корне неверный подход. Конечно, никто не заставляет еще в восьмом классе выбирать будущий колледж или даже ВУЗ, но задуматься о будущем необходимо заранее.</p>
        <p>Все дело в том, что выбор профессии должен формироваться у ребенка годами, складываться из сонма различных интересов и личных достижений.</p>
        <p>Если вовремя акцентировать внимание на увлечениях, помогать в развитии талантов и навыков, то у подростка не возникнет проблем с определением будущей профессии.</p>
        <p>Родителям необходимо помочь, а не препятствовать воле детей. Но об этом мы поговорим немного позже.</p>
        <h3>Важно понимать, что работа — это не обязательно дело всей жизни.</h3>
        <p>Но она должна быть направлена на развитие качеств личности, а не на зарабатывание денег или потакание чужим прихотям.</p>
        <p>На что нужно обращать внимание перед выбором будущей работы:
        <ul>
            <li>Любимые занятия</li>
            <li>Творческие навыки</li>
            <li>Сфера углубленного интереса</li>
            <li>Желание посвящать занятию большую часть свободного времени</li>
        </ul>
        </p>
        <p>Если сложно вычленить одно увлечение из множества равноценных, необходимо отделить занятия для формирования будущего от хобби.</p>
        <h3>Принципиальные отличия работы от хобби</h3>
        <p>Решив определиться с профессией, каждый подросток должен понимать одну простую истину: работа не является хобби.</p>
        <p>На первый взгляд, ничего нового в этом утверждении нет, но множество юношей и девушек по всему миру совершают эту ошибку — выбирают профессию исходя исключительно из своих увлечений. Приведем пример.</p>
        <p>Ребенок любит животных, ему нравится о них заботиться. На основе одного этого факта он выбирает профессию ветеринара, не учитывая особенностей этой медицинской специализации.</p>
        <p>А потом разочаровывается, оказавшись не с любимыми животными, а в пыльных аудиториях с учебниками по анатомии и на далеких от романтизма клинических практиках.</p>
        <p>Чтобы отличить увлечение от чего-то более серьезного, можно использовать различные кружки и дополнительные занятия.</p>
        <p>То есть чтобы научиться рисовать, не обязательно становиться дизайнером, а при желании стать модельером сперва стоит записаться на курсы кройки и шитья.</p>
        <p>Итак, попробуем сформировать список предложений о том, как отличить хобби от работы, которые помогут подростку определиться с будущей профессией:
        <ul>
            <li>Понять, насколько сильно вы любите то, чем хотели бы заниматься</li>
            <li>Прочесть специализированную литературу о профессии</li>
            <li>Записаться на сопряженные с профессией курсы</li>
            <li>Сходить на несколько открытых лекций по специализации в университет</li>
        </ul>
        </p>
        <p>На самом деле, последний пункт отлично подходит и для тех, кто уже твердо обозначил свое будущее занятие.</p>
        <p>Чаще всего студенты бросают учебу именно на первом курсе, поскольку она кардинально отличается от их ожиданий.</p>
        <h3>Методы выбора профессии</h3>
        <p>После того как все хобби были отделены от желаемой специальности, необходимо провести дополнительные тесты, чтобы определить насколько подростку подходит выбранная профессия.</p>
        <p>Существует много способов, которые позволяют быстро и доступно выяснить, в какой области можно себя реализовать. Мы расскажем о наиболее оптимальных.</p>
